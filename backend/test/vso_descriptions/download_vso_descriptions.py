for vso_id in vso_list:
    try: 
        work_item = wit_client.get_work_item(vso_id, expand='all')
        soup_description = BeautifulSoup(work_item.fields['System.Description'], 'html.parser')
        text_description = soup_description.get_text(separator=', ').strip()

        if not os.path.isfile(f"{vso_id}.txt"):
            with open(f"{vso_id}.txt", "w", encoding="utf-8") as file:
                file.write(text_description)
    except Exception as e:
        print(f"Error processing {vso_id}: {e}")